# Operador de suma (+)
suma = 5 + 3
print(f"Suma: {suma}")  # Imprime: Suma: 8

# Operador de resta (-)
resta = 10 - 6
print(f"Resta: {resta}")  # Imprime: Resta: 4

# Operador de multiplicación (*)
multiplicacion = 7 * 7
print(f"Multiplicación: {multiplicacion}")  # Imprime: Multiplicación: 49

# Operador de división (/)
division = 20 / 4
print(f"División: {division}")  # Imprime: División: 5.0

# Operador de módulo (%)
modulo = 10 % 3
print(f"Módulo: {modulo}")  # Imprime: Módulo: 1

# Operador de división entera (//)
division_entera = 10 // 3
print(f"División Entera: {division_entera}")  # Imprime: División Entera: 3

# Operador de exponenciación (**)
exponenciacion = 2 ** 3
print(f"Exponenciación: {exponenciacion}")  # Imprime: Exponenciación: 8

