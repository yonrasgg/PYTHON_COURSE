# Definir las variables
num1 = 15
num2 = 4

# Operaciones aritméticas con variables
suma = num1 + num2
resta = num1 - num2
producto = num1 * num2
division = num1 / num2
division_entera = num1 // num2
modulo = num1 % num2

# Resultados
print('Suma: ', suma)
print('Resta: ', resta)
print('Producto: ', producto)
print('División: ', division)
print('División entera: ', division_entera)
print('Módulo: ', modulo)
