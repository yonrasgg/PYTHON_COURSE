import math # Importa el módulo math

print(math.pi) # Imprime el valor de pi (3.141592653589793) 
print(math.e) # Imprime el valor de e (2.718281828459045)   
print(math.sin(math.pi / 2)) # Imprime el seno de pi / 2 (1.0)  
print(math.cos(math.pi / 2)) # Imprime el coseno de pi / 2 (6.123233995736766e-17)  
print(math.tan(math.pi / 4)) # Imprime la tangente de pi / 4 (0.9999999999999999)   
print(math.log(math.e)) # Imprime el logaritmo natural de e (1.0)   
print(math.pow(2, 3)) # Imprime 2 elevado a la 3 (8.0)  
print(math.sqrt(9)) # Imprime la raíz cuadrada de 9 (3.0)   
print(math.factorial(5)) # Imprime el factorial de 5 (120)  
print(math.fabs(-5)) # Imprime el valor absoluto de -5 (5.0)    
print(math.ceil(4.5)) # Imprime el entero más pequeño mayor o igual que 4.5 (5) 
print(math.floor(4.5)) # Imprime el entero más grande menor o igual que 4.5 (4) 
print(math.trunc(4.5)) # Imprime la parte entera de 4.5 (4) 
print(math.degrees(math.pi / 2)) # Imprime el valor de pi / 2 en grados (90.0)  
print(math.radians(90)) # Imprime el valor de 90 en radianes (1.5707963267948966)   
print(math.copysign(2, -3)) # Imprime el valor absoluto de 2 con el signo de -3 (-2.0)  
print(math.gcd(24, 36)) # Imprime el máximo común divisor de 24 y 36 (12)   
print(math.isfinite(2)) # Imprime True si 2 es finito (True)    
print(math.isinf(2)) # Imprime True si 2 es infinito (False)    
print(math.isnan(2)) # Imprime True si 2 es NaN (False) 
print(math.modf(2.5)) # Imprime la parte fraccionaria y la parte entera de 2.5 ((0.5, 2.0)) 
print(math.frexp(2)) # Imprime la mantisa y el exponente de 2 ((0.5, 2))    
print(math.ldexp(2, 3)) # Imprime el valor de 2 * 2 elevado a la 3 (16.0)   
print(math.exp(2)) # Imprime el valor de e elevado a la 2 (7.38905609893065)    
print(math.expm1(2)) # Imprime el valor de e elevado a la 2 - 1 (6.38905609893065)  
print(math.log(2, 3)) # Imprime el logaritmo de 2 en base 3 (0.6309297535714574)    
print(math.log1p(2)) # Imprime el logaritmo natural de 2 + 1 (1.0986122886681098)   
print(math.log2(2)) # Imprime el logaritmo de 2 en base 2 (1.0) 
print(math.log10(2)) # Imprime el logaritmo de 2 en base 10 (0.3010299956639812)    
print(math.pow(math.e, 2)) # Imprime el valor de e elevado a la 2 (7.38905609893065)